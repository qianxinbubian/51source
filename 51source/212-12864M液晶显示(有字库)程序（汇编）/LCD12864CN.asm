FLASH:
            MOV  A,#08H       ;关闭显示
            LCALL SEND_I
            LCALL  DELAY5
            MOV  A,#0CH	  ;开显示,关光标,不闪烁
	    LCALL SEND_I
            LCALL  DELAY5
            MOV  A,#08H       ;关闭显示
            LCALL SEND_I
            LCALL  DELAY5
            MOV  A,#0CH	  ;开显示,关光标,不闪烁
	    LCALL SEND_I
            LCALL  DELAY5
            MOV  A,#08H       ;关闭显示
            LCALL SEND_I
            LCALL  DELAY5
                RET
;==================================================
;清屏
;==================================================
CLEAR_P:
            MOV	A,#01H           ;清屏
	    LCALL SEND_I
            MOV	A,#34H
	    LCALL SEND_I
	    MOV	A,#30H
	    LCALL SEND_I
            RET
;==================================================
;查表取数据送显示
;==================================================
QUSHU:
            CLR	A
	    MOVC A,@A+DPTR       ;查表取数据
	    LCALL SEND_D          ;送显示
	    INC	DPTR
            LCALL DELAY4           ;延时80ms,
            DJNZ COUNT,QUSHU
	    RET

;===============================================
;写数据子程序
;RS=1,RW=0,E=高脉冲,D0-D7=数据
;===============================================
SEND_D:
	    LCALL	CHK_BUSY    ;写数据子程序
	    SETB	RS
	    CLR	RW
	    MOV	P0,A
	    SETB	E
	    NOP
	    NOP
	    CLR	E
	    RET
;===============================================
;写指令子程序
;RS=0,RW=0,E=高脉冲,D0-D7=指令码
;===============================================
SEND_I:
	    LCALL CHK_BUSY
	    CLR	RS
	    CLR	RW
	    MOV	P0,A
	    SETB E
	    NOP
	    NOP
	    CLR	E
	    RET
;================================================
;读数据子程序
;RS=1,RW=1,E=H,D0-D7=数据
;================================================
READ_D:
	   LCALL CHK_BUSY    ;读数据子程序
	   SETB	RS
	   SETB	RW
	   SETB	E
	   NOP
	   MOV	A,P0
	   CLR	E
	   MOV	STORE,A
	   RET
;================================================
;;测忙碌子程序
;RS=0,RW=1,E=H,D0-D7=状态字
;================================================
CHK_BUSY:
    	   MOV	P0,#0FFH    ;测忙碌子程序
	   CLR	RS
	   SETB	RW
	   SETB	E
	   JB	P0.7,$
	   CLR	E
	   RET
;================================================
;延时子程序
;================================================
DELAY3:
	   MOV	R5,#16H
DEL31:	   MOV	R6,#0FFH
DEL32:	   MOV	R7,#0FFH
DEL33:	   DJNZ	R7,DEL33
	   DJNZ	R6,DEL32
	   DJNZ	R5,DEL31
	   RET

DELAY2:
           MOV	R6,#0CH
DEL21:	   MOV	R7,#18H
DEL22:	   DJNZ	R7,DEL22
	   DJNZ	R6,DEL21
	   RET

DELAY1:
           MOV	R6,#06H
DEL11:	   MOV	R7,#08H
DEL12:	   DJNZ	R7,DEL12
	   DJNZ	R6,DEL11
           RET

DELAY4:
           MOV	R6,#200
DEL41:	   MOV	R7,#200
DEL42:	   DJNZ	R7,DEL42
	   DJNZ	R6,DEL41
	   RET

DELAY5:
           MOV	R5,#05H
DEL51:	   MOV	R6,#0FFH
DEL52:	   MOV	R7,#0FFH
DEL53:	   DJNZ	R7,DEL53
	   DJNZ	R6,DEL52
	   DJNZ	R5,DEL51
	   RET
